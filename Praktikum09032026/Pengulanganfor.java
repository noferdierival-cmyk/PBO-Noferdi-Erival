public class Pengulanganfor {
   
    public static void main(String[] args){
        System.out.println("Perulangan 1-10");
        for(int i=1;i<=10;i++)
        {
            System.out.print("\t"+i);
        }
        System.out.println("\nPerulangan 10-1");
        for(int i=10;i>=1;i--)
        {
            System.out.print("\t"+i);
        }
    }
}
